public class ch_2literal {
    public static void main(String[] args){
        byte age = 23;// value assign to variable is Literal
        char name = 'A';
        float num = 5.5f;
        long num2 = 66666666l;
        System.out.println(age);
        System.out.println(name);
        System.out.println(num);
        System.out.println(num2);
        
    }
    
}
