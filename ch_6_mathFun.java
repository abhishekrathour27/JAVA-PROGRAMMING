public class ch_6_mathFun {
    public static void main(String[] args) {
        // maximum in two
        System.out.println("Maximum in two number");
        System.out.println(Math.max(23, 33));
        // minimum in two
        System.out.println("Minimum in two number");
        System.out.println(Math.min(6,8));
        //Square root
        System.out.println("Square root of number");
        System.out.println(Math.sqrt(49));
        //
    }
}
